import time

def simulate_conversation():
    print("Researcher: I am looking up the latest data on OpenCLOW...")
    time.sleep(1)
    print("Writer: I will take that data and create a summary.")
    time.sleep(1)
    print("Reviewer: Checking the summary for accuracy...")
    time.sleep(1)
    print("System: Multi-agent task completed successfully!")

if __name__ == "__main__":
    simulate_conversation()
